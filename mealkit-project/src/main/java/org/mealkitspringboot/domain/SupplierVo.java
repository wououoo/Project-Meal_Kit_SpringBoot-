package org.mealkitspringboot.domain;

public class SupplierVo {
	private int sup_id;
	private String sup_name;
	private String phone_num;
	private String sup_address;
	private String sup_email;
	private String sup_sell;
	public int getSup_id() {
		return sup_id;
	}
	public void setSup_id(int sup_id) {
		this.sup_id = sup_id;
	}
	public String getSup_name() {
		return sup_name;
	}
	public void setSup_name(String sup_name) {
		this.sup_name = sup_name;
	}
	public String getPhone_num() {
		return phone_num;
	}
	public void setPhone_num(String phone_num) {
		this.phone_num = phone_num;
	}
	public String getSup_address() {
		return sup_address;
	}
	public void setSup_address(String sup_address) {
		this.sup_address = sup_address;
	}
	public String getSup_email() {
		return sup_email;
	}
	public void setSup_email(String sup_email) {
		this.sup_email = sup_email;
	}
	public String getSup_sell() {
		return sup_sell;
	}
	public void setSup_sell(String sup_sell) {
		this.sup_sell = sup_sell;
	}
	
	

}
